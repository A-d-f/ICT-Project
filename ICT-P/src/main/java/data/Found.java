package data;

public class Found {
	
	String found;
	
	public Found() {
		
	}

	/**
	 * @param found
	 */
	public Found(String found) {
		super();
		this.found = found;
	}

	/**
	 * @return the found
	 */
	public String getFound() {
		return found;
	}

	/**
	 * @param found the found to set
	 */
	public void setFound(String found) {
		this.found = found;
	}
	
	public String toString() {
		return found;
		
	}

}
